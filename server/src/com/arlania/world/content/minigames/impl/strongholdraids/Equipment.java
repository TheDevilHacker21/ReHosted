    public static double damageBonus(Player p) {

        double damageMultiplier = 0;

        for(int i = 0; i < 14; i++) {
            for (int j = 0; j < 30; j++) {

                if (p.getEquipment().forSlot(i).getId() == lowEquipment[j] || p.getEquipment().forSlot(i).getId() == bindedLowEquipment[j]) {
                    damageMultiplier += .03;

                    if (j % 2 == 1)
                        damageMultiplier += .03;
                }

                else if (p.getEquipment().forSlot(i).getId() == mediumEquipment[j] || p.getEquipment().forSlot(i).getId() == bindedMediumEquipment[j]) {
                    damageMultiplier += .09;

                    if (j % 2 == 1)
                        damageMultiplier += .03;
                }

                else if (p.getEquipment().forSlot(i).getId() == highEquipment[j] || p.getEquipment().forSlot(i).getId() == bindedHighEquipment[j]) {
                    damageMultiplier += .15;

                    if (j % 2 == 1)
                        damageMultiplier += .03;
                }

                else if (p.getEquipment().forSlot(i).getId() == legendaryEquipment[j] || p.getEquipment().forSlot(i).getId() == bindedLegendaryEquipment[j]) {
                    damageMultiplier += .21;

                    if (j % 2 == 1)
                        damageMultiplier += .03;
                }

                else if (p.getEquipment().forSlot(i).getId() == masterEquipment[j] || p.getEquipment().forSlot(i).getId() == bindedMasterEquipment[j]) {
                    damageMultiplier += .27;

                    if (j % 2 == 1)
                        damageMultiplier += .03;
                }

                else if (j < 15 && (p.getEquipment().forSlot(i).getId() == bisEquipment[j] || p.getEquipment().forSlot(i).getId() == bindedBisEquipment[j])) {
                    damageMultiplier += .33;

                    if (j % 2 == 1)
                        damageMultiplier += .03;
                }
            }
        }

        return damageMultiplier;
    }


